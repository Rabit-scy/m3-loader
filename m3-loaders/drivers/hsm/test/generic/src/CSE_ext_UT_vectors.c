void CSE_ext_valTest_generateMessageAddressInit_startBufAdd(uint32_t P_messageNb,
                                                            CSE_ext_valTest_MAddr_stt P_messageAdd,
                                                            CSE_ext_valTest_MAddr_stt * P_pMessageWrongAdd,
                                                            memRangeDescriptor_stt P_memRangeAdd, uint32_t P_verbose)
{

    P_pMessageWrongAdd->mpM1Add = (uint32_t*)P_messageAdd.mpM1Add;
    P_pMessageWrongAdd->mpM2Add = (uint32_t*)P_messageAdd.mpM2Add;
    P_pMessageWrongAdd->mpM3Add = (uint32_t*)P_messageAdd.mpM3Add;
    P_pMessageWrongAdd->mpM4Add = (uint32_t*)P_messageAdd.mpM4Add;
    P_pMessageWrongAdd->mpM5Add = (uint32_t*)P_messageAdd.mpM5Add;

    switch (P_messageNb)
    {
        case 1:
            P_pMessageWrongAdd->mpM1Add = (uint32_t*)(P_memRangeAdd.mMemStartAdd - 1);
            break;
        case 2:
            P_pMessageWrongAdd->mpM2Add = (uint32_t*)(P_memRangeAdd.mMemStartAdd - 1);
            break;

        case 3:
            P_pMessageWrongAdd->mpM3Add = (uint32_t*)(P_memRangeAdd.mMemStartAdd - 1);
            break;

        case 4:
            P_pMessageWrongAdd->mpM4Add = (uint32_t*)(P_memRangeAdd.mMemStartAdd - 1);
            break;

        case 5:
            P_pMessageWrongAdd->mpM5Add = (uint32_t*)(P_memRangeAdd.mMemStartAdd - 1);
            break;
    }

    if (P_verbose)
    {
        printf("Write Message @ address 0x%08x - 1 of %s: ", P_memRangeAdd.mMemStartAdd, P_memRangeAdd.mMemName);
    }

} /* End of CSE_ext_valTest_generateMessageAddressInit_startBufAdd */

